package day02;

/*
    关系运算符：
        >, <, ==, !=, >=, <=

     (比较运算符）关系运算符的结果一定是boolean类型的值
*/

public class GuanXiDemo1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
